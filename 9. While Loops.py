'''
    In 'while' loop we give a condtion, till the condition is True the loops gets executed

    while(condition):

'''

# Printing 1 to 50 using while loop.

i = 1

while(i < 51):
    print(i)
    i += 1


#  Printing "Mohit" 5 times using while loop.

i = 1

while (i<=5):
    print("Mohit")
    i += 1



# print the content of the List using while loop.

l = [1,2,3,4,5,6,7,8,9]

idx = 0

while (idx < len(l)):
    print(l[idx])
    idx += 1